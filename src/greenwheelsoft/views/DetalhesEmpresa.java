    public void preenche(){
        this.nome.setText(this.c.getNome());
        this.endereco.setText( this.c.getEndereco() );
        this.lblEstoque.setText("Estoque em " +this.c.getInventario().getInventario().size());
        
        if(this.c.getInventario().getInventario().size() > 0)
            
            this.c.getInventario().identificaIguais().forEach( (key, value)-> 
                this.estoque.append( 
                        String.valueOf(value) + " " + key + " nas cores:\n" 
                            + this.c.getInventario().coresByNome(key) + "\n\n"
                )
            );
        else{
            this.estoque.append("Estoque Vazio !!!");
        }
        
        
    }

