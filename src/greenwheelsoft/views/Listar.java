    private void tblExistentesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblExistentesMouseClicked
        // TODO add your handling code here:
        if(evt.getClickCount() == 2){
            
            if(this.tblExistentes.getModel() instanceof CarroExistenteTableModel || this.tblExistentes.getModel() instanceof CarroPatioTableModel){
                
                Carro c = this.carrosModelTable.getCarro(this.tblExistentes.getSelectedRow());
                DetalhesCarro dc = new DetalhesCarro(this, false);
                dc.defineCarro(c);
                dc.setVisible(true);
            }
            
            if( this.tblExistentes.getModel() instanceof EmpresaModelTable ){
                Concessionaria c = this.empresasModelTable.getEmpresa( this.tblExistentes.getSelectedRow() );
                DetalhesEmpresa de = new  DetalhesEmpresa(this, false);
                de.defineEmpresa(c);
                de.setVisible(true);
            }
        }
    }//GEN-LAST:event_tblExistentesMouseClicked

