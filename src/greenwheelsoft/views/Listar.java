    public void setModel(ArrayList<Concessionaria> lista){
        this.empresasModelTable = new EmpresaModelTable();
        
        for(Concessionaria c : lista)
            empresasModelTable.addEmpresa(c);
        
        this.empresasModelTable.fireTableDataChanged();
        this.tblExistentes.setModel(this.empresasModelTable);

        this.empresasModelTable.formatTable(tblExistentes);
        this.changeLB("Empresas Cadastradas " + lista.size());
    }
    
    
    public void setModel(ArrayList<Carro> lista, boolean patio){
        this.lista = lista;
        System.out.println(lista.size());
        if(!patio)
            this.carrosModelTable = new CarroExistenteTableModel();
        else
            this.carrosModelTable = new CarroPatioTableModel();
        
        
        for(Carro u : lista)
            this.carrosModelTable.addCarro( u );
       
//        this.carrosModelTable.fireTableDataChanged();
        this.tblExistentes.setModel(this.carrosModelTable);

        this.carrosModelTable.formatTable(tblExistentes);
        this.changeLB("Total de Carros Disponiveis: " + this.lista.size());
        
    }
    
